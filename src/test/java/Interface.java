/**
 * The Interface Interface.
 */
public interface Interface {

    /**
     * Gets the string.
     *
     * @return the string
     */
    public String getString();

    /**
     * Gets the integer.
     *
     * @return the integer
     */
    public Integer getInteger();

    /**
     * Gets the primitive int.
     *
     * @return the primitive int
     */
    public int getPrimitiveInt();

    /**
     * Gets the primitive bool.
     *
     * @return the primitive bool
     */
    public boolean getPrimitiveBool();

    /**
     * Gets the null.
     *
     * @return the null
     */
    public Object getNull();
}
