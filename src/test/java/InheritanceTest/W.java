package InheritanceTest;

/**
 * The Class W.
 */
class W implements B {

    /** {@inheritDoc} */
    public void a() {
        System.out.println("W.a()");
    }

    /** {@inheritDoc} */
    public void b() {
        System.out.println("W.b()");
    }

    /**
     * W.
     */
    public void w() {
        System.out.println("W.w()");
    }

    /**
     * W.
     */
    void w_() {
        System.out.println("W.w_()");
    }
}
