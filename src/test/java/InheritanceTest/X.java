package InheritanceTest;

/**
 * The Class X.
 */
public class X extends W {

    /** {@inheritDoc} */
    @Override
    public void a() {
        System.out.println("X.a()");
    }

    /**
     * X.
     */
    public void x() {
        System.out.println("X.x()");
    }

    /**
     * X.
     */
    void x_() {
        System.out.println("X.x_()");
    }
}
